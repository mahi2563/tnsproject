package com.tnsif.demo.day1;

public class ArithmeticOperators {

	public static void main(String[] args) {
		   // int datatype
        int a = 20, b = 10;
        System.out.println("INT OPERATIONS");
        System.out.println("Addition: " + (a + b));
        System.out.println("Subtraction: " + (a - b));
        System.out.println("Multiplication: " + (a * b));
        System.out.println("Division: " + (a / b));
        System.out.println("Modulus: " + (a % b));

        // float datatype
        float x = 15.5f, y = 4.5f;
        System.out.println("\nFLOAT OPERATIONS");
        System.out.println("Addition: " + (x + y));
        System.out.println("Subtraction: " + (x - y));
        System.out.println("Multiplication: " + (x * y));
        System.out.println("Division: " + (x / y));

        // double datatype
        double m = 50.25, n = 20.10;
        System.out.println("\nDOUBLE OPERATIONS");
        System.out.println("Addition: " + (m + n));
        System.out.println("Subtraction: " + (m - n));
        System.out.println("Multiplication: " + (m * n));
        System.out.println("Division: " + (m / n));

	}

}